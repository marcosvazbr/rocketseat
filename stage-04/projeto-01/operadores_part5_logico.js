// Operadores logicos (logical operators)

// - 2 valores booleanos, quando verificados, resultará em verdadeiro ou falto (true o false)


let pao = true
let queijo = true

// and && (os dois tem que ser verdadeiros)
console.log('and = &&')
console.log(pao && queijo)

// or || (quando um for verdadeiro a comparação é verdadeira)
console.log('or = ||')
pao = false

console.log(pao || queijo)

// not ! (mudar o valor lógico, se e falso vira verdadeiro se é verdadeiro vira falso)
console.log('NOT = !')
pao = false


console.log(!queijo)












       