// Operadores de comparação

// Irá comparar valores e retornar um boolean como resposta à comparação 

let one = 1
let two = 2

// ==    igual a
console.log('igual a ==')
console.log(two == 1)
console.log(one == '1')
console.log(one == 1)

// !=    diferente de
console.log('Diferente de !=')
console.log(two != one)
console.log(one != '1')
console.log(one != 1)

// === estritamente igual a  (vai comparar os valores e os tipos de elemento)
console.log('=== estritamente igual a')
console.log(one === '1')
console.log(one === 1)

// !==  estritamente diferente de 
console.log('!== estritamente diferente de')
console.log(one !== '1')
console.log(one !== 1)


// >   Maior que 
console.log('> Maior que')
console.log(one > two)


// >=   Maior igual a
console.log(' >=    Maior igual a')
console.log(one >= one)
console.log(two >= one)

// <    Menor que
console.log(' < Menor que')
console.log(one < two)

// <+    Menor igual a
console.log(' <= Menor igual a')
console.log(one <= two)
console.log(one <= one)











       